def p(data):
    tmp = bin(data)[2:].zfill(32)
    # print(tmp)
    # tmp1 = [0]*32
    # for i in range(32):
    #     tmp1[P[i]-1] = tmp[i]
    ans = '0b'
    for i in range(32):
        ans = ans + tmp[P[i]-1]
        # print(ans)
    return hex(int(ans,2))[2:].zfill(8)
    
